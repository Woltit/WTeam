package com.wteam.backend.common.enums;

import lombok.Getter;

/**
 * Системні ролі користувачів додатка, що використовуються для контролю доступу (авторизації).
 * <p>
 * Кожна роль визначає рівень прав користувача в системі. Цей енам мапиться на
 * нативний тип даних {@code role} у базі даних PostgreSQL.
 * </p>
 */
@Getter
public enum Role {
    /**
     * Адміністратор системи.
     * <p>
     * Має максимальний рівень доступу: керування користувачами, перегляд фінансових транзакцій,
     * керування категоріями та повний доступ до конфігурацій платформи.
     * </p>
     */
    ADMIN,

    /**
     * Модератор платформи.
     * <p>
     * Відповідає за перевірку документів користувачів, зміну статусів верифікації профілів,
     * а також арбітраж у диспутах між орендарями та власниками речей.
     * Назва константи відповідає значенню {@code MODER} у схемі БД.
     * </p>
     */
    MODER,

    /**
     * Звичайний користувач системи.
     * <p>
     * Роль за замовчуванням для всіх нових акаунтів. Дозволяє створювати власні оголошення,
     * бронювати речі інших користувачів, писати повідомлення в чатах та залишати відгуки.
     * </p>
     */
    USER
}
