package com.wteam.backend.common.enums;

/**
 * Статуси верифікації персонального профілю користувача модератором платформи.
 * <p>
 * Визначає поточний стан перевірки документів користувача. Цей енам мапиться на
 * нативний тип даних {@code verification_status} у базі даних PostgreSQL.
 * </p>
 *
 * @see com.wteam.backend.user_profile.UserProfile
 */
public enum VerificationStatus {

    /**
     * Профіль очікує на перевірку.
     * <p>
     * Початковий статус, який присвоюється автоматично після того, як користувач
     * заповнив розширені дані профілю (паспорт, телефон тощо) та відправив їх на модерацію.
     * </p>
     */
    PENDING,

    /**
     * Профіль успішно верифіковано.
     * <p>
     * Модератор підтвердив достовірність даних. Користувач отримує повний доступ
     * до функціоналу платформи (наприклад, можливість публікувати власні речі для оренди).
     * </p>
     */
    VERIFIED,

    /**
     * Верифікацію відхилено.
     * <p>
     * Надані дані або документи не пройшли перевірку (наприклад, нечітке фото або помилка в даних).
     * Користувачу зазвичай надсилається сповіщення з причиною відхилення для можливості повторної подачі.
     * </p>
     */
    REJECTED
}
